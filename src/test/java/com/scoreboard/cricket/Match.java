package com.scoreboard.cricket;

public class Match {

	private String de;

	private String id;
	
	private String si;
	
	private String t1;
	
	private String t2;
	
	Match() {

	}
	
	public String getDe() {
		return de;
	}

	public void setDe(String de) {
		this.de = de;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSi() {
		return si;
	}

	public void setSi(String si) {
		this.si = si;
	}
	
	public String getT1() {
		return t1;
	}

	public void setT1(String t1) {
		this.t1 = t1;
	}

	public String getT2() {
		return t2;
	}

	public void setT2(String t2) {
		this.t2 = t2;
	}

}
